import AppLayout from '@/layouts/app-layout';
import { Head, Link } from '@inertiajs/react';
import { BreadcrumbItem, Prediction } from '@/types';
import { Card, CardContent } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Badge } from '@/components/ui/badge';
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table';
import { Eye, AlertTriangle, CheckCircle, HelpCircle, Activity, Trash2 } from 'lucide-react';
import { router } from '@inertiajs/react';

interface IndexProps {
    predictions: {
        data: Prediction[];
        current_page: number;
        last_page: number;
        next_page_url: string | null;
        prev_page_url: string | null;
        total: number;
    };
}

export default function Index({ predictions }: IndexProps) {
    const breadcrumbs: BreadcrumbItem[] = [
        { title: 'Dashboard', href: '/dashboard' },
        { title: 'Predictions', href: '/predictions' },
    ];

    const getRiskBadge = (level: string) => {
        switch (level) {
            case 'low':
                return <Badge variant="outline" className="bg-green-50 text-green-700 border-green-200">Low Risk</Badge>;
            case 'medium':
                return <Badge variant="outline" className="bg-yellow-50 text-yellow-700 border-yellow-200">Medium Risk</Badge>;
            case 'high':
                return <Badge variant="destructive">High Risk</Badge>;
            case 'critical':
                return <Badge variant="destructive" className="animate-pulse">CRITICAL</Badge>;
            default:
                return <Badge variant="outline">{level}</Badge>;
        }
    };

    return (
        <AppLayout breadcrumbs={breadcrumbs}>
            <Head title="Prediction History" />

            <div className="flex flex-1 flex-col gap-6 p-4">
                <div className="flex justify-between items-center">
                    <div>
                        <h1 className="text-3xl font-bold">Prediction History</h1>
                        <p className="text-muted-foreground mt-1">
                            History of failure predictions generated by AI models.
                        </p>
                    </div>
                    <Button asChild>
                        <Link href="/predictions/create">
                            <Activity className="mr-2 h-4 w-4" />
                            Generate New Prediction
                        </Link>
                    </Button>
                </div>

                <Card>
                    <CardContent className="p-0">
                        <Table>
                            <TableHeader>
                                <TableRow>
                                    <TableHead>Date</TableHead>
                                    <TableHead>Device</TableHead>
                                    <TableHead>Provider</TableHead>
                                    <TableHead>Failure Prob.</TableHead>
                                    <TableHead>Risk Level</TableHead>
                                    <TableHead>Confidence</TableHead>
                                    <TableHead className="text-right">Actions</TableHead>
                                </TableRow>
                            </TableHeader>
                            <TableBody>
                                {predictions.data.map((prediction) => (
                                    <TableRow key={prediction.id}>
                                        <TableCell>
                                            <div className="flex flex-col">
                                                <span className="font-medium">
                                                    {new Date(prediction.created_at).toLocaleDateString()}
                                                </span>
                                                <span className="text-xs text-muted-foreground">{new Date(prediction.created_at).toLocaleTimeString()}</span>
                                            </div>
                                        </TableCell>
                                        <TableCell>
                                            <Link href={`/medical-devices/${prediction.medical_device_id}`} className="hover:underline text-blue-600 font-medium">
                                                {prediction.medicalDevice?.name || (prediction as any).medical_device?.name || 'Unknown Device'}
                                            </Link>
                                        </TableCell>
                                        <TableCell>
                                            <div className="flex flex-col">
                                                <span className="text-sm">{prediction.provider?.name}</span>
                                                <span className="text-xs text-muted-foreground">{prediction.provider?.provider}</span>
                                            </div>
                                        </TableCell>
                                        <TableCell>
                                            <span className={`font-bold ${prediction.failure_probability > 75 ? 'text-red-500' :
                                                prediction.failure_probability > 50 ? 'text-orange-500' : 'text-green-500'
                                                }`}>
                                                {prediction.failure_probability}%
                                            </span>
                                        </TableCell>
                                        <TableCell>{getRiskBadge(prediction.risk_level)}</TableCell>
                                        <TableCell>
                                            <div className="flex items-center gap-1 text-sm text-muted-foreground">
                                                {prediction.confidence_score}%
                                            </div>
                                        </TableCell>
                                        <TableCell className="text-right">
                                            <div className="flex justify-end gap-2">
                                                <Button variant="ghost" size="sm" asChild>
                                                    <Link href={`/predictions/${prediction.id}`}>
                                                        <Eye className="h-4 w-4 mr-1" />
                                                        Details
                                                    </Link>
                                                </Button>
                                                <Button
                                                    variant="ghost"
                                                    size="sm"
                                                    className="text-red-500 hover:text-red-700 hover:bg-red-50"
                                                    onClick={() => {
                                                        if (confirm('Are you sure you want to delete this prediction?')) {
                                                            router.delete(`/predictions/${prediction.id}`);
                                                        }
                                                    }}
                                                >
                                                    <Trash2 className="h-4 w-4" />
                                                </Button>
                                            </div>
                                        </TableCell>
                                    </TableRow>
                                ))}
                                {!predictions.data.length && (
                                    <TableRow>
                                        <TableCell colSpan={7} className="text-center py-8 text-muted-foreground">
                                            No predictions found.
                                            <Link href="/predictions/create" className="ml-1 text-blue-500 hover:underline">Generate one now.</Link>
                                        </TableCell>
                                    </TableRow>
                                )}
                            </TableBody>
                        </Table>
                    </CardContent>
                </Card>
            </div>
        </AppLayout>
    );
}
